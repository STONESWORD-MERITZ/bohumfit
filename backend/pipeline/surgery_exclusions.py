# BOHUMFIT-062: 수술로 오분류되는 '비수술' 코드명 전역 제외 집합.
# - 수술 감지 알고리즘은 불변. 이 집합에 든 코드명만 수술 분류에서 제외한다.
# - 항목 추가는 NON_SURGERY_NAMES 한 곳에만 추가하면 된다(확장 용이).
# - 매칭: 공백 제거 후 exact 비교(표기 공백 차이에 견고).
import re


def _norm(name: str) -> str:
    return re.sub(r"\s+", "", name or "")


# 비수술 코드명(원문) — 공백은 _norm 에서 제거되므로 가독성 위해 원문 그대로 둔다.
NON_SURGERY_NAMES = {
    _norm("수액제주입로를통한주사"),                    # 수액 경로 주사(주입), 수술 아님
    _norm("치관수복물또는보철물의 제거[1치당]-간단한것"),  # 치과 보철물 제거 처치
    _norm("치관수복물또는보철물의 제거[1치당]-복잡한것"),  # 치과 보철물 제거 처치
    _norm("후두내주입"),                                # 후두 내 약물 주입, 수술 아님
}


def is_non_surgery_excluded(name: str) -> bool:
    """수술 분류에서 전역 제외할 비수술 코드명인지."""
    return _norm(name) in NON_SURGERY_NAMES


# ── BOHUMFIT-059: 검사·처치·주사 등 '비수술 행위' 키워드 제외 ──────────────────
# 세부진료(detail) 행위명이 검사/처치/주사인데 수술로 오탐되는 문제를 막는다.
#   예) 복부초음파(검사)·병변내주입요법(주사) → 수술 미판정.
# 단, 강수술 신호(절제·적출·봉합 등)가 있으면 비수술로 보지 않는다(진짜 수술 누락 0).
#   대조군: 비용적출술(적출)·창상봉합술(봉합) → 강수술 신호로 제외되지 않음.

# 강수술 신호 — 하나라도 있으면 is_non_surgery_action 은 False(수술로 유지).
_STRONG_SURGERY_KEYWORDS = (
    "수술", "절제", "절개", "적출", "봉합", "이식", "절단", "재건",
    "치환", "관혈", "배농", "성형", "정복", "박리", "소파", "결찰",
    "유합", "고정술", "발치", "색전", "용종절제", "폴립절제", "낭종절제",
)

# 비수술 행위 신호(검사·처치·주사/주입). _norm 으로 공백 제거 후 부분일치.
_NON_SURGERY_ACTION_KEYWORDS = (
    # 검사(영상·진단) — "검사"는 강수술 신호(절제·적출 등)가 없을 때만 비수술로 본다.
    "검사", "초음파", "촬영", "생검", "판독", "엑스레이", "X-RAY", "X-ray",
    "방사선단순", "도플러", "심전도", "골밀도", "내시경검사",
    # 처치
    "드레싱", "소독", "단순처치", "물리치료", "재활치료", "주입로를통한",
    # 주사/주입(비수술적)
    "병변내주입", "주입요법", "주사요법", "관절강내주사", "건초내주사",
    "경막외주사", "근육내주사", "정맥내주사", "피하주사",
)


def is_non_surgery_action(name: str) -> bool:
    """검사·처치·주사 등 '비수술 행위'명인지. 강수술 신호가 있으면 False(수술 유지).

    BOHUMFIT-059: detail 수술 판정(컬럼/키워드)의 오탐 제거용. 누락 0을 위해
    강수술 키워드(절제·적출·봉합 등)가 하나라도 있으면 비수술로 보지 않는다.
    """
    c = _norm(name)
    if not c:
        return False
    cl = c.lower()
    if any(_norm(k).lower() in cl for k in _STRONG_SURGERY_KEYWORDS):
        return False
    return any(_norm(k).lower() in cl for k in _NON_SURGERY_ACTION_KEYWORDS)
